     * @return void
     */
    public function loadRowValues($rs = null)
    {
        if (is_array($rs)) {
            $row = $rs;
        } elseif ($rs && property_exists($rs, "fields")) { // Recordset
            $row = $rs->fields;
        } else {
            $row = $this->newRow();
        }
        if (!$row) {
            return;
        }

        // Call Row Selected event
        $this->rowSelected($row);
        $this->id->setDbValue($row['id']);
        $this->crop_id->setDbValue($row['crop_id']);
        $this->recommendation->setDbValue($row['recommendation']);
        $this->affiliate_links->setDbValue($row['affiliate_links']);
        $this->thumbnail_url->Upload->DbValue = $row['thumbnail_url'];
        $this->thumbnail_url->setDbValue($this->thumbnail_url->Upload->DbValue);
        $this->updated_at->setDbValue($row['updated_at']);
        $this->created_at->setDbValue($row['created_at']);
    }

