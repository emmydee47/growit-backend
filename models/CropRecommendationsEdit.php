    // Validate form
    protected function validateForm()
    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        $validateForm = true;
        if ($this->id->Required) {
            if (!$this->id->IsDetailKey && EmptyValue($this->id->FormValue)) {
                $this->id->addErrorMessage(str_replace("%s", $this->id->caption(), $this->id->RequiredErrorMessage));
            }
        }
        if (!CheckGuid($this->id->FormValue)) {
            $this->id->addErrorMessage($this->id->getErrorMessage(false));
        }
        if ($this->crop_id->Required) {
            if (!$this->crop_id->IsDetailKey && EmptyValue($this->crop_id->FormValue)) {
                $this->crop_id->addErrorMessage(str_replace("%s", $this->crop_id->caption(), $this->crop_id->RequiredErrorMessage));
            }
        }
        if ($this->recommendation->Required) {
            if (!$this->recommendation->IsDetailKey && EmptyValue($this->recommendation->FormValue)) {
                $this->recommendation->addErrorMessage(str_replace("%s", $this->recommendation->caption(), $this->recommendation->RequiredErrorMessage));
            }
        }
        if ($this->affiliate_links->Required) {
            if (!$this->affiliate_links->IsDetailKey && EmptyValue($this->affiliate_links->FormValue)) {
                $this->affiliate_links->addErrorMessage(str_replace("%s", $this->affiliate_links->caption(), $this->affiliate_links->RequiredErrorMessage));
            }
        }
        if ($this->thumbnail_url->Required) {
            if ($this->thumbnail_url->Upload->FileName == "" && !$this->thumbnail_url->Upload->KeepFile) {
                $this->thumbnail_url->addErrorMessage(str_replace("%s", $this->thumbnail_url->caption(), $this->thumbnail_url->RequiredErrorMessage));
            }
        }
        if ($this->updated_at->Required) {
            if (!$this->updated_at->IsDetailKey && EmptyValue($this->updated_at->FormValue)) {
                $this->updated_at->addErrorMessage(str_replace("%s", $this->updated_at->caption(), $this->updated_at->RequiredErrorMessage));
            }
        }
        if (!CheckDate($this->updated_at->FormValue, $this->updated_at->formatPattern())) {
            $this->updated_at->addErrorMessage($this->updated_at->getErrorMessage(false));
        }
        if ($this->created_at->Required) {
            if (!$this->created_at->IsDetailKey && EmptyValue($this->created_at->FormValue)) {
                $this->created_at->addErrorMessage(str_replace("%s", $this->created_at->caption(), $this->created_at->RequiredErrorMessage));
            }
        }
        if (!CheckDate($this->created_at->FormValue, $this->created_at->formatPattern())) {
            $this->created_at->addErrorMessage($this->created_at->getErrorMessage(false));
        }

        // Return validate result
        $validateForm = $validateForm && !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

    // Update record based on key values
    protected function editRow()
    {
        global $Security, $Language;
        $oldKeyFilter = $this->getRecordFilter();
        $filter = $this->applyUserIDFilters($oldKeyFilter);
        $conn = $this->getConnection();

        // Load old row
        $this->CurrentFilter = $filter;
        $sql = $this->getCurrentSql();
        $rsold = $conn->fetchAssociative($sql);
        if (!$rsold) {
            $this->setFailureMessage($Language->phrase("NoRecord")); // Set no record message
            return false; // Update Failed
        } else {
            // Save old values
            $this->loadDbValues($rsold);
        }

        // Set new row
        $rsnew = [];

        // id
        $this->id->setDbValueDef($rsnew, $this->id->CurrentValue, "{00000000-0000-0000-0000-000000000000}", $this->id->ReadOnly);

        // crop_id
        $this->crop_id->setDbValueDef($rsnew, $this->crop_id->CurrentValue, "{00000000-0000-0000-0000-000000000000}", $this->crop_id->ReadOnly);

        // recommendation
        $this->recommendation->setDbValueDef($rsnew, $this->recommendation->CurrentValue, "", $this->recommendation->ReadOnly);

        // affiliate_links
        $this->affiliate_links->setDbValueDef($rsnew, $this->affiliate_links->CurrentValue, null, $this->affiliate_links->ReadOnly);

        // thumbnail_url
        if ($this->thumbnail_url->Visible && !$this->thumbnail_url->ReadOnly && !$this->thumbnail_url->Upload->KeepFile) {
            $this->thumbnail_url->Upload->DbValue = $rsold['thumbnail_url']; // Get original value
            if ($this->thumbnail_url->Upload->FileName == "") {
                $rsnew['thumbnail_url'] = null;
            } else {
                $rsnew['thumbnail_url'] = $this->thumbnail_url->Upload->FileName;
            }
        }

        // updated_at
        $this->updated_at->setDbValueDef($rsnew, UnFormatDateTime($this->updated_at->CurrentValue, $this->updated_at->formatPattern()), null, $this->updated_at->ReadOnly);

        // created_at
        $this->created_at->setDbValueDef($rsnew, UnFormatDateTime($this->created_at->CurrentValue, $this->created_at->formatPattern()), null, $this->created_at->ReadOnly);

        // Update current values
        $this->setCurrentValues($rsnew);

        // Check field with unique index (id)
        if ($this->id->CurrentValue != "") {
            $filterChk = "(\"id\" = '" . AdjustSql($this->id->CurrentValue, $this->Dbid) . "')";
            $filterChk .= " AND NOT (" . $filter . ")";
            $this->CurrentFilter = $filterChk;
            $sqlChk = $this->getCurrentSql();
            $rsChk = $conn->executeQuery($sqlChk);
            if (!$rsChk) {
                return false;
            }
            if ($rsChk->fetch()) {
                $idxErrMsg = str_replace("%f", $this->id->caption(), $Language->phrase("DupIndex"));
                $idxErrMsg = str_replace("%v", $this->id->CurrentValue, $idxErrMsg);
                $this->setFailureMessage($idxErrMsg);
                return false;
            }
        }
        if ($this->thumbnail_url->Visible && !$this->thumbnail_url->Upload->KeepFile) {
            $oldFiles = EmptyValue($this->thumbnail_url->Upload->DbValue) ? [] : [$this->thumbnail_url->htmlDecode($this->thumbnail_url->Upload->DbValue)];
            if (!EmptyValue($this->thumbnail_url->Upload->FileName)) {
                $newFiles = [$this->thumbnail_url->Upload->FileName];
                $NewFileCount = count($newFiles);
                for ($i = 0; $i < $NewFileCount; $i++) {
                    if ($newFiles[$i] != "") {
                        $file = $newFiles[$i];
                        $tempPath = UploadTempPath($this->thumbnail_url, $this->thumbnail_url->Upload->Index);
                        if (file_exists($tempPath . $file)) {
                            if (Config("DELETE_UPLOADED_FILES")) {
                                $oldFileFound = false;
                                $oldFileCount = count($oldFiles);
                                for ($j = 0; $j < $oldFileCount; $j++) {
                                    $oldFile = $oldFiles[$j];
                                    if ($oldFile == $file) { // Old file found, no need to delete anymore
                                        array_splice($oldFiles, $j, 1);
                                        $oldFileFound = true;
                                        break;
                                    }
                                }
                                if ($oldFileFound) { // No need to check if file exists further
                                    continue;
                                }
                            }
                            $file1 = UniqueFilename($this->thumbnail_url->physicalUploadPath(), $file); // Get new file name
                            if ($file1 != $file) { // Rename temp file
                                while (file_exists($tempPath . $file1) || file_exists($this->thumbnail_url->physicalUploadPath() . $file1)) { // Make sure no file name clash
                                    $file1 = UniqueFilename([$this->thumbnail_url->physicalUploadPath(), $tempPath], $file1, true); // Use indexed name
                                }
                                rename($tempPath . $file, $tempPath . $file1);
                                $newFiles[$i] = $file1;
                            }
                        }
                    }
                }
                $this->thumbnail_url->Upload->DbValue = empty($oldFiles) ? "" : implode(Config("MULTIPLE_UPLOAD_SEPARATOR"), $oldFiles);
                $this->thumbnail_url->Upload->FileName = implode(Config("MULTIPLE_UPLOAD_SEPARATOR"), $newFiles);
                $this->thumbnail_url->setDbValueDef($rsnew, $this->thumbnail_url->Upload->FileName, null, $this->thumbnail_url->ReadOnly);
            }
        }

        // Call Row Updating event
        $updateRow = $this->rowUpdating($rsold, $rsnew);

        // Check for duplicate key when key changed
        if ($updateRow) {
            $newKeyFilter = $this->getRecordFilter($rsnew);
            if ($newKeyFilter != $oldKeyFilter) {
                $rsChk = $this->loadRs($newKeyFilter)->fetch();
                if ($rsChk !== false) {
                    $keyErrMsg = str_replace("%f", $newKeyFilter, $Language->phrase("DupKey"));
                    $this->setFailureMessage($keyErrMsg);
                    $updateRow = false;
                }
            }
        }
        if ($updateRow) {
            if (count($rsnew) > 0) {
                $this->CurrentFilter = $filter; // Set up current filter
                $editRow = $this->update($rsnew, "", $rsold);
            } else {
                $editRow = true; // No field to update
            }
            if ($editRow) {
                if ($this->thumbnail_url->Visible && !$this->thumbnail_url->Upload->KeepFile) {
                    $oldFiles = EmptyValue($this->thumbnail_url->Upload->DbValue) ? [] : [$this->thumbnail_url->htmlDecode($this->thumbnail_url->Upload->DbValue)];
                    if (!EmptyValue($this->thumbnail_url->Upload->FileName)) {
                        $newFiles = [$this->thumbnail_url->Upload->FileName];
                        $newFiles2 = [$this->thumbnail_url->htmlDecode($rsnew['thumbnail_url'])];
                        $newFileCount = count($newFiles);
                        for ($i = 0; $i < $newFileCount; $i++) {
                            if ($newFiles[$i] != "") {
                                $file = UploadTempPath($this->thumbnail_url, $this->thumbnail_url->Upload->Index) . $newFiles[$i];
                                if (file_exists($file)) {
                                    if (@$newFiles2[$i] != "") { // Use correct file name
                                        $newFiles[$i] = $newFiles2[$i];
                                    }
                                    if (!$this->thumbnail_url->Upload->SaveToFile($newFiles[$i], true, $i)) { // Just replace
                                        $this->setFailureMessage($Language->phrase("UploadErrMsg7"));
                                        return false;
                                    }
                                }
                            }
                        }
                    } else {
                        $newFiles = [];
                    }
                    if (Config("DELETE_UPLOADED_FILES")) {
                        foreach ($oldFiles as $oldFile) {
                            if ($oldFile != "" && !in_array($oldFile, $newFiles)) {
                                @unlink($this->thumbnail_url->oldPhysicalUploadPath() . $oldFile);
                            }
                        }
                    }
                }
            }
        } else {
            if ($this->getSuccessMessage() != "" || $this->getFailureMessage() != "") {
                // Use the message, do nothing
            } elseif ($this->CancelMessage != "") {
                $this->setFailureMessage($this->CancelMessage);
                $this->CancelMessage = "";
            } else {
                $this->setFailureMessage($Language->phrase("UpdateCancelled"));
            }
            $editRow = false;
        }

        // Call Row_Updated event
        if ($editRow) {
            $this->rowUpdated($rsold, $rsnew);
        }

        // Clean upload path if any
        if ($editRow) {
            // thumbnail_url
            CleanUploadTempPath($this->thumbnail_url, $this->thumbnail_url->Upload->Index);
        }

        // Write JSON for API request
        if (IsApi() && $editRow) {
            $row = $this->getRecordsFromRecordset([$rsnew], true);
            WriteJson(["success" => true, $this->TableVar => $row]);
        }
        return $editRow;
    }

